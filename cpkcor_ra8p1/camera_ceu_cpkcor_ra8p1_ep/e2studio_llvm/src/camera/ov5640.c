static uint32_t softReset(void)
{
	uint8_t val;
	uint32_t err;

	IIC_READ_CHECK(0x3103, &val);
	val &= ~(0x01 << 1);
	IIC_WRITE_CHECK(0x3103, val);
	IIC_WRITE_CHECK(0x3008, 0x82);
	R_BSP_SoftwareDelay(300, BSP_DELAY_UNITS_MILLISECONDS);

	return err;
}

uint32_t OV5640_GetID(uint16_t *id)
{
	uint8_t val;
	fsp_err_t err;

	IIC_READ_CHECK(0x300A, &val);
	*id = (uint16_t)(((uint16_t)val) << 8);
	IIC_READ_CHECK(0x300B, &val);
	*id |= val;

	return err;
}

uint32_t OV5640_GetOutputSize(uint16_t *length, uint16_t *width)
{
	uint8_t reg3808;
    uint8_t reg3809;
    uint8_t reg380A;
    uint8_t reg380B;
	fsp_err_t err;

	R_BSP_SoftwareDelay(100, BSP_DELAY_UNITS_MILLISECONDS);
	IIC_READ_CHECK(0x3808, &reg3808);
	IIC_READ_CHECK(0x3809, &reg3809);
	IIC_READ_CHECK(0x380A, &reg380A);
	IIC_READ_CHECK(0x380B, &reg380B);

	*length = (uint16_t)((reg3808 & 0x0F) << 8) | reg3809;
	*width = (uint16_t)((reg380A & 0x07) << 8) | reg380B;

	return err;
}

uint32_t OV5640_Init(CameraInterfaceEnum interface)
{
	uint16_t i;
	uint16_t init_len;
	fsp_err_t err;
#if __OV5640_DEBUG
	uint8_t val;
#endif

	const struct OV5640Reg *p_init_table = NULL;

	pinConfig(interface);
	hardReset(interface);
	err = softReset();
	if (err) {
	#if __OV5640_DEBUG
		LOG_E(__FUNCTION__, "softReset() failed: %u", err);
	#endif
		return err;
	}
	R_BSP_SoftwareDelay(200, BSP_DELAY_UNITS_MILLISECONDS);

#if __OV5640_DEBUG
	err = OV5640_GetID(&i);
	if (err) {
		LOG_E(__FUNCTION__, "OV5640_GetID() failed: %u", err);
		return err;
	}
	LOG_I(__FUNCTION__, "ID: 0x%04X", i);
#endif

	if (interface == CAMERA_INTERFACE_DVP) {
		IIC_WRITE_CHECK(0x3103, 0x11);
		IIC_WRITE_CHECK(0x3008, 0x82);
		R_BSP_SoftwareDelay(200, BSP_DELAY_UNITS_MILLISECONDS);
		R_BSP_SoftwareDelay(200, BSP_DELAY_UNITS_MILLISECONDS);
		init_len = (uint16_t)(sizeof(sc_init_table_dvp) / sizeof(sc_init_table_dvp[0]));
		p_init_table = sc_init_table_dvp;
	}
	else {
		init_len = (uint16_t)(sizeof(sc_init_table_mipi) / sizeof(sc_init_table_mipi[0]));
		p_init_table = sc_init_table_mipi;
	}

#if __OV5640_DEBUG
    LOG_D(__FUNCTION__, "Init table length: %u", init_len);
#endif
    for (i = 0; i < init_len; i++) {
    	IIC_WRITE_CHECK(p_init_table[i].reg, p_init_table[i].val);
	#if __OV5640_DEBUG
    	IIC_READ_CHECK(p_init_table[i].reg, &val);
    	if (val != p_init_table[i].val) {
    		LOG_W(__FUNCTION__, "REG 0x%X: W[0x%X] != R[0x%X]", p_init_table[i].reg, p_init_table[i].val, val);
    	}
	#endif
    }
    R_BSP_SoftwareDelay(500, BSP_DELAY_UNITS_MILLISECONDS);

    if (interface == CAMERA_INTERFACE_MIPI_CSI) {
		/* Config OV5640 MIPI clock */
		val = (OV5640_SYS_CLK_DIV << 4) | OV5640_MIPI_SCALE_DIV;
		IIC_WRITE_CHECK(0x3035, val);
	#if __OV5640_DEBUG
		LOG_D(__FUNCTION__, "Write REG 0x3035: 0x%X", val);
	#endif
		IIC_WRITE_CHECK(0x3036, OV5640_PLL_MULTIPLIER);
	#if __OV5640_DEBUG
		LOG_D(__FUNCTION__, "Write REG 0x3036: 0x%X", OV5640_PLL_MULTIPLIER);
	#endif
		val = (OV5640_ROOT_DIVIDER_PLL << 4) | OV5640_PLL_PRE_DIVIDER;
		IIC_WRITE_CHECK(0x3037, val);
	#if __OV5640_DEBUG
		LOG_D(__FUNCTION__, "Write REG 0x3037: 0x%X", val);
	#endif
		val = (OV5640_ROOT_DIVIDER_PCLK << 4) | (OV5640_ROOT_DIVIDER_SCLK2X << 2) | OV5640_ROOT_DIVIDER_SCLK;
		IIC_WRITE_CHECK(0x3108, val);
	#if __OV5640_DEBUG
		LOG_D(__FUNCTION__, "Write REG 0x3108: 0x%X", val);
	#endif
		uint32_t base_pll = OV5640_MIPI_XCLK_HZ / ((OV5640_ROOT_DIVIDER_PLL + 1) * OV5640_PLL_PRE_DIVIDER) * OV5640_PLL_MULTIPLIER;
		s_pclk_hz = base_pll / OV5640_ROOT_DIVIDER_PCLK;
		if (s_pclk_hz > OV5640_MAX_PCLK_HZ) {
			s_pclk_hz = OV5640_MAX_PCLK_HZ;
		}
	#if __OV5640_DEBUG
		LOG_D(__FUNCTION__, "base_pll: %u Hz, pclk: %u Hz", base_pll, s_pclk_hz);
	#endif
		OV5640_SetFPS(60, NULL);
		OV5640_SetMIPIChannel(0);
    }
    else {
		OV5640_SetOutputFormat(OUTPUT_FORMAT_RGB565);
		OV5640_SetAutoFocus(1);
		R_BSP_SoftwareDelay(100, BSP_DELAY_UNITS_MILLISECONDS);
		OV5640_SetExposureLevel(EXPOSURE_LEVEL_8);
    }

	return err;
}

uint32_t OV5640_IsImageReady(CameraInterfaceEnum interface, uint8_t **p_image)
{
	if (interface == CAMERA_INTERFACE_DVP) {
		if (s_ceu_ready) {
			s_ceu_ready = 0;
			return 1;
		}
		else {
			return 0;
		}
	}
	else {
		if (s_vin_ready) {
			s_vin_ready = 0;
			if (p_image != NULL) {
				*p_image = s_ready_buf;
			}
			return 1;
		}
		else {
			return 0;
		}
	}
}

uint32_t OV5640_IsPowerDown(CameraInterfaceEnum interface)
{
	bsp_io_port_pin_t pin;

	if (interface == CAMERA_INTERFACE_DVP) {
		pin = OV5640_PIN_CEU_PWR_ON;
	}
	else {
		pin = OV5640_PIN_VIN_PWR_ON;
	}

	if (R_BSP_PinRead(pin) == BSP_IO_LEVEL_HIGH) {
		return 1;
	}
	else {
		return 0;
	}
}

uint32_t OV5640_PowerDown(CameraInterfaceEnum interface, uint8_t enable)
{
	R_BSP_PinAccessEnable();
	if (interface == CAMERA_INTERFACE_DVP) {
		if (enable) {
			R_BSP_PinWrite(OV5640_PIN_CEU_PWR_ON, BSP_IO_LEVEL_HIGH);
		}
		else {
			R_BSP_PinWrite(OV5640_PIN_CEU_PWR_ON, BSP_IO_LEVEL_LOW);
			/* PWDN 后 OV5640 内部时钟、计数器会停止，IO 进入高阻态，需要时间恢复，否则前几帧会读到全黑的图像 */
			R_BSP_SoftwareDelay(100, BSP_DELAY_UNITS_MILLISECONDS);
		}
	}
	else {
		if (enable) {
			R_BSP_PinWrite(OV5640_PIN_VIN_PWR_ON, BSP_IO_LEVEL_HIGH);
		}
		else {
			R_BSP_PinWrite(OV5640_PIN_VIN_PWR_ON, BSP_IO_LEVEL_LOW);
			/* PWDN 后 OV5640 内部时钟、计数器会停止，IO 进入高阻态，需要时间恢复，否则前几帧会读到全黑的图像 */
			R_BSP_SoftwareDelay(100, BSP_DELAY_UNITS_MILLISECONDS);
		}
	}
	R_BSP_PinAccessDisable();

	return 0;
}

uint32_t OV5640_SetAutoFocus(uint8_t enable)
{
	uint8_t val;
	uint16_t i, firmware_len;
	fsp_err_t err;

	uint16_t addr = OV5640_FIRMWARE_ADDR;
	uint16_t timeout = 5000;

	if (enable) {
		IIC_WRITE_CHECK(0x3000, 0x20);
		firmware_len = (uint16_t)(sizeof(sc_auto_focus_firmware) / sizeof(sc_auto_focus_firmware[0]));
	#if __OV5640_DEBUG
		LOG_D(__FUNCTION__, "Firmware length: %u", firmware_len);
	#endif
		for (i = 0; i < firmware_len; i++) {
			IIC_WRITE_CHECK(addr, sc_auto_focus_firmware[i]);
			addr++;
		}
		IIC_WRITE_CHECK(0x3022, 0x00);
		IIC_WRITE_CHECK(0x3023, 0x00);
		IIC_WRITE_CHECK(0x3024, 0x00);
		IIC_WRITE_CHECK(0x3025, 0x00);
		IIC_WRITE_CHECK(0x3026, 0x00);
		IIC_WRITE_CHECK(0x3027, 0x00);
		IIC_WRITE_CHECK(0x3028, 0x00);
		IIC_WRITE_CHECK(0x3029, 0x7F);
		IIC_WRITE_CHECK(0x3000, 0x00);

		IIC_READ_CHECK(0x3029, &val);
		while (val != 0x70) {
			R_BSP_SoftwareDelay(1, BSP_DELAY_UNITS_MILLISECONDS);
			IIC_READ_CHECK(0x3029, &val);
			if (timeout) {
				timeout--;
			}
			else {
				err = FSP_ERR_TIMEOUT;
				break;
			}
		}
	}
	else {
		err = FSP_ERR_UNSUPPORTED;
	}

	return err;
}

